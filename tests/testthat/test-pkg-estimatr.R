requiet("estimatr")

test_that("lm_robust()", {
  data(mtcars)
  m1 <- lm_robust(mpg ~ gear + wt + cyl, data = mtcars)

  # model_info
  expect_true(model_info(m1)$is_linear)

  # find_predictors
  expect_identical(find_predictors(m1), list(conditional = c("gear", "wt", "cyl")))
  expect_identical(find_predictors(m1, flatten = TRUE), c("gear", "wt", "cyl"))
  expect_null(find_predictors(m1, effects = "random"))

  # find_random
  expect_null(find_random(m1))

  # get_random
  expect_warning(get_random(m1))

  # find_response
  expect_identical(find_response(m1), "mpg")

  # get_response
  expect_equal(get_response(m1), mtcars$mpg)

  # get_predictors
  expect_equal(colnames(get_predictors(m1)), c("gear", "wt", "cyl"))

  # link_inverse
  expect_equal(link_inverse(m1)(.2), .2, tolerance = 1e-5)

  # get_data
  expect_equal(nrow(get_data(m1)), 32)
  expect_equal(colnames(get_data(m1)), c("mpg", "gear", "wt", "cyl"))

  # find_formula
  expect_length(find_formula(m1), 1)
  expect_equal(
    find_formula(m1),
    list(conditional = as.formula("mpg ~ gear + wt + cyl")),
    ignore_attr = TRUE
  )

  # find_terms
  expect_equal(find_terms(m1), list(
    response = "mpg",
    conditional = c("gear", "wt", "cyl")
  ))
  expect_equal(find_terms(m1, flatten = TRUE), c("mpg", "gear", "wt", "cyl"))

  # n_obs
  expect_equal(n_obs(m1), 32)

  # link_function
  expect_equal(link_function(m1)(.2), .2, tolerance = 1e-5)

  # link_inverse
  expect_equal(link_inverse(m1)(.2), .2, tolerance = 1e-5)

  # find_parameters
  expect_equal(
    find_parameters(m1),
    list(conditional = c("(Intercept)", "gear", "wt", "cyl"))
  )
  expect_equal(nrow(get_parameters(m1)), 4)
  expect_equal(
    get_parameters(m1)$Parameter,
    c("(Intercept)", "gear", "wt", "cyl")
  )

  # is_multivariate
  expect_false(is_multivariate(m1))

  # find_algorithm
  expect_warning(expect_null(find_algorithm(m1)))

  # find_statistic
  expect_identical(find_statistic(m1), "t-statistic")
}


test_that("iv_robust()", {
  data(mtcars)
  m1 <- iv_robust(mpg ~ gear + cyl | carb + wt, data = mtcars)

  # model_info
  expect_true(model_info(m1)$is_linear)

  # find_predictors
  expect_identical(
    find_predictors(m1),
    list(
      conditional = c("gear", "cyl"),
      instruments = c("carb", "wt")
    )
  )
  expect_identical(
    find_predictors(m1, component = "instruments"),
    list(instruments = c("carb", "wt"))
  )
  expect_identical(
    find_predictors(m1, flatten = TRUE),
    c("gear", "cyl", "carb", "wt")
  )
  expect_null(find_predictors(m1, effects = "random"))

  # find_random
  expect_null(find_random(m1))

  # get_random
  expect_warning(get_random(m1))

  # find_response
  expect_identical(find_response(m1), "mpg")

  # get_response
  expect_equal(get_response(m1), mtcars$mpg)

  # get_predictors
  expect_equal(colnames(get_predictors(m1)), c("gear", "cyl", "carb", "wt"))

  # get_data
  expect_equal(nrow(get_data(m1)), 32)
  expect_equal(
    colnames(get_data(m1)),
    c("mpg", "carb + wt", "gear", "cyl", "carb", "wt")
  )

  # find_formula
  expect_length(find_formula(m1), 2)
  expect_equal(
    find_formula(m1),
    list(
      conditional = as.formula("mpg ~ gear + cyl"),
      instruments = as.formula("~carb + wt")
    ),
    ignore_attr = TRUE
  )

  # find_terms
  expect_equal(
    find_terms(m1),
    list(
      response = "mpg",
      conditional = c("gear", "cyl"),
      instruments = c("carb", "wt")
    )
  )
  expect_equal(
    find_terms(m1, flatten = TRUE),
    c("mpg", "gear", "cyl", "carb", "wt")
  )

  # n_obs
  expect_equal(n_obs(m1), 32)

  # link_function
  expect_equal(link_function(m1)(.2), .2, tolerance = 1e-5)

  # link_inverse
  expect_equal(link_inverse(m1)(.2), .2, tolerance = 1e-5)

  # find_parameters
  expect_equal(
    find_parameters(m1),
    list(conditional = c("(Intercept)", "gear", "cyl"))
  )
  expect_equal(nrow(get_parameters(m1)), 3)
  expect_equal(
    get_parameters(m1)$Parameter,
    c("(Intercept)", "gear", "cyl")
  )

  # is_multivariate
  expect_false(is_multivariate(m1))

  # find_algorithm
  expect_warning(expect_null(find_algorithm(m1)))

  # find_statistic
  expect_identical(find_statistic(m1), "t-statistic")
})
